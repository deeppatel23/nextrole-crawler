    source_type = (source_cfg.get("source_type") or "HTML").upper()
    site_key = str(source_cfg.get("site_number") or "default")
    max_saved = _resolve_max_saved(source_cfg, site_key)
    last_saved = source_cfg.get("last_saved")
    location = str(source_cfg.get("location") or DEFAULT_LOCATION)

    today = datetime.utcnow().date()
    if last_saved:
        try:
            last_saved_date = datetime.strptime(last_saved, "%Y-%m-%d").date()
            if today <= last_saved_date:
                print(
                    f"{company_label}: last_saved={last_saved} is >= today={today.isoformat()}, skipping."
                )
                return 0
        except ValueError:
            print(f"{company_label}: invalid last_saved={last_saved}, continuing.")

    checker = MongoJobHashChecker()
    created_at = today.isoformat()
    total_saved = 0
    page = 1

    print(f"{company_label}: starting crawl for location={location}.")
    while True:
        if total_saved >= max_saved:
            print(f"{company_label}: reached max_saved_jobs={max_saved} for site={site_key}, stopping.")
            break

        try:
            page_html = _fetch_jobs_page(location=location, page=page)
        except Exception as exc:
            print(f"{company_label}: failed to fetch page={page}: {exc}")
            break

        if "No results" in page_html:
            print(f"{company_label}: received 'No results' at page={page}, stopping.")
            break

        jobs = _parse_jobs_from_html(page_html)
        if not jobs:
            print(f"{company_label}: no jobs found at page={page}, stopping.")
            break

        print(f"{company_label}: iteration {page} fetched {len(jobs)} jobs.")

        stop_all = False
        for job in jobs:
            if total_saved >= max_saved:
                print(f"{company_label}: reached max_saved_jobs={max_saved} for site={site_key}, stopping.")
                stop_all = True
                break

            job_id = _normalize_whitespace(job.get("job_id"))
            title = _normalize_whitespace(job.get("title"))
            raw_href = _normalize_whitespace(job.get("href"))
            qualifications = _normalize_whitespace(job.get("qualifications_text"))

            if not job_id or not title or not raw_href:
                continue

            apply_link = urljoin(APPLY_BASE_URL, raw_href)

            enrichment = get_enrichment(
                title,
                apply_link,
                qualifications,
            )

            location_values = job.get("location_values")
            normalized_locations: List[str] = []
            if isinstance(location_values, list):
                normalized_locations = [
                    _normalize_whitespace(str(value))
                    for value in location_values
                    if _normalize_whitespace(str(value))
                ]

            if not normalized_locations:
                fallback_location = _normalize_whitespace(job.get("location_text"))
                if fallback_location:
                    normalized_locations = [fallback_location]

            if not normalized_locations:
                normalized_locations = [""]

            for location_value in normalized_locations:
                if total_saved >= max_saved:
                    print(f"{company_label}: reached max_saved_jobs={max_saved} for site={site_key}, stopping.")
                    stop_all = True
                    break

                city, state, country = _extract_location_parts(location_value)
                city_slug = _normalize_city_for_id(city)
                location_job_id = f"{job_id}-{city_slug}"
                hash_key = f"{raw_href}|{city_slug}"
                job_hash = generate_job_hash(company, hash_key)

                if OUTPUT_DESTINATION == "MONGO" and checker.exists(job_hash):
                    print(
                        f"{company_label}: existing job_hash found for href={raw_href} city={city or city_slug}, stopping subsequent jobs."
                    )
                    stop_all = True
                    break

                role = RoleDetail(
                    job_hash=job_hash,
                    job_id=location_job_id,
                    company=company,
                    source_type=source_type,
                    title=title,
                    role=None,
                    category=enrichment["category"],
                    min_yoe=enrichment["min_yoe"],
                    max_yoe=enrichment["max_yoe"],
                    city=city,
                    state=state,
                    country=country,
                    workplace_type=None,
                    skills=enrichment["skills"],
                    apply_link=apply_link,
                    created_at=created_at,
                    updated_at=None,
                )

                saved_count, stop_fetch = append_roles(OUTPUT_FILE, [role])
                if saved_count:
                    total_saved += saved_count
                    checker.record(job_hash)
                    print(f"{company_label}: saved job_id={location_job_id}")

                if stop_fetch:
                    print(
                        f"{company_label}: duplicate detected while saving href={raw_href} city={city or city_slug}, stopping subsequent jobs."
                    )
                    stop_all = True
                    break

            if stop_all:
                break

        if stop_all:
            break

        if len(jobs) < DEFAULT_PAGE_SIZE:
            print(
                f"{company_label}: fetched less than page size ({len(jobs)}<{DEFAULT_PAGE_SIZE}) at page={page}, stopping."
            )
            break

        page += 1

    print(f"{company_label}: total saved {total_saved} jobs.")
    return total_saved
