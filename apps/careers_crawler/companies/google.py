class _GoogleJobsHTMLParser(HTMLParser):
    def __init__(self) -> None:
        super().__init__()
        self.jobs: List[Dict[str, Any]] = []

        self._ul_depth = 0
        self._in_jobs_list = False

        self._current_job: Optional[Dict[str, Any]] = None
        self._job_li_depth = 0

        self._in_title = False
        self._location_depth = 0
        self._qualifications_depth = 0
        self._location_group_depth = 0
        self._in_location_item = False
        self._location_item_parts: List[str] = []

        self._title_parts: List[str] = []
        self._location_parts: List[str] = []
        self._qualifications_parts: List[str] = []
        self._location_values: List[str] = []

    @staticmethod
    def _attr_dict(attrs: List[Tuple[str, Optional[str]]]) -> Dict[str, str]:
        out: Dict[str, str] = {}
        for key, value in attrs:
            if value is not None:
                out[key] = value
        return out

    @staticmethod
    def _has_class(attrs: List[Tuple[str, Optional[str]]], class_name: str) -> bool:
        for key, value in attrs:
            if key != "class" or not value:
                continue
            classes = {part.strip() for part in value.split() if part.strip()}
            if class_name in classes:
                return True
        return False

    def handle_starttag(self, tag: str, attrs: List[Tuple[str, Optional[str]]]) -> None:
        if tag == "ul":
            if self._has_class(attrs, "spHGqe"):
                self._in_jobs_list = True
                self._ul_depth = 1
            elif self._in_jobs_list:
                self._ul_depth += 1

        if not self._in_jobs_list:
            return

        if self._current_job is None and tag == "li" and self._has_class(attrs, "lLd3Je"):
            attr_map = self._attr_dict(attrs)
            self._current_job = {
                "job_id": _normalize_whitespace(attr_map.get("ssk")),
                "title": None,
                "location_text": None,
                "location_values": [],
                "qualifications_text": None,
                "href": None,
            }
            self._job_li_depth = 1
            self._title_parts = []
            self._location_parts = []
            self._qualifications_parts = []
            self._location_values = []
            return

        if self._current_job is None:
            return

        if tag == "li":
            self._job_li_depth += 1

        if tag == "h3" and self._has_class(attrs, "QJPWVe"):
            self._in_title = True
            return

        if tag == "span" and self._has_class(attrs, "pwO9Dc") and self._has_class(attrs, "vo5qdf"):
            self._location_group_depth = 1
            return
        if self._location_group_depth > 0:
            self._location_group_depth += 1

        if tag == "span" and self._location_group_depth > 0 and self._has_class(attrs, "r0wTof"):
            self._in_location_item = True
            self._location_item_parts = []
            return

        if tag == "div" and self._has_class(attrs, "EAcu5e"):
            self._location_depth = 1
            return
        if self._location_depth > 0:
            self._location_depth += 1

        if tag == "div" and self._has_class(attrs, "Xsxa1e"):
            self._qualifications_depth = 1
            return
        if self._qualifications_depth > 0:
            self._qualifications_depth += 1

        if tag == "a":
            attr_map = self._attr_dict(attrs)
            href = attr_map.get("href")
            if href and not self._current_job.get("href"):
                self._current_job["href"] = _normalize_whitespace(href)

    def handle_endtag(self, tag: str) -> None:
        if tag == "ul" and self._in_jobs_list:
            self._ul_depth -= 1
            if self._ul_depth <= 0:
                self._in_jobs_list = False
                self._ul_depth = 0

        if not self._current_job:
            return

        if tag == "h3" and self._in_title:
            self._in_title = False

        if tag == "span" and self._in_location_item:
            location_value = _normalize_whitespace(" ".join(self._location_item_parts))
            if location_value:
                self._location_values.append(location_value)
            self._in_location_item = False
            self._location_item_parts = []

        if self._location_group_depth > 0:
            self._location_group_depth -= 1
            if self._location_group_depth < 0:
                self._location_group_depth = 0

        if self._location_depth > 0:
            self._location_depth -= 1
            if self._location_depth < 0:
                self._location_depth = 0

        if self._qualifications_depth > 0:
            self._qualifications_depth -= 1
            if self._qualifications_depth < 0:
                self._qualifications_depth = 0

        if tag == "li":
            self._job_li_depth -= 1
            if self._job_li_depth == 0:
                self._current_job["title"] = _normalize_whitespace(" ".join(self._title_parts))
                self._current_job["location_text"] = _normalize_whitespace(" ".join(self._location_parts))
                self._current_job["location_values"] = list(dict.fromkeys(self._location_values))
                self._current_job["qualifications_text"] = _normalize_whitespace(
                    " ".join(self._qualifications_parts)
                )
                self.jobs.append(self._current_job)
                self._current_job = None
                self._title_parts = []
                self._location_parts = []
                self._qualifications_parts = []
                self._location_values = []

    def handle_data(self, data: str) -> None:
        if not self._current_job:
            return
        if not data or not data.strip():
            return

        if self._in_title:
            self._title_parts.append(data)
        if self._in_location_item:
            self._location_item_parts.append(data)
        if self._location_depth > 0:
            self._location_parts.append(data)
        if self._qualifications_depth > 0:
            self._qualifications_parts.append(data)


def _fetch_jobs_page(location: str, page: int) -> str:
    params = {
        "location": location,
        "sort_by": "date",
    }
    if page > 1:
        params["page"] = str(page)

    url = f"{LISTING_URL}?{urlencode(params)}"
    print(f"Google: start iteration {page} calling {url}")
    response = requests.get(
        LISTING_URL,
        params=params,
        headers={"User-Agent": "Mozilla/5.0"},
        timeout=30,
    )
    response.raise_for_status()
    return response.text


def _parse_jobs_from_html(html_text: str) -> List[Dict[str, Any]]:
    parser = _GoogleJobsHTMLParser()
    parser.feed(html_text)
    parser.close()
    return parser.jobs


